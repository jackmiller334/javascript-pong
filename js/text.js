var text = {};

text.drawScore = function(canvasContext, score, positionX, positionY, size) {
	if(score < 10) {
		var initialY = positionY;
		var scoreBlocks = numbers[score];
		for(var i = 0; i < 3; ++i) {
			positionY = initialY;
			for(var j = 0; j < 5; ++j) {
				if(scoreBlocks[j*3 + i] == 1) {
					canvasContext.fillStyle = "white"
					canvasContext.fillRect(positionX,positionY,size,size);
					canvasContext.stroke();
				}
				positionY += size;
			}
			positionX += size;
		}
	}
};

text.drawText = function(canvasContext, text, positionX, positionY, size, spacing) {
	var initialY = positionY;
	for(var i = 0; i < text.length; ++i) {
		var letter = letters[text.charAt(i).toLowerCase()];
		for(var j = 0; j < 5; ++j) {
			positionY = initialY;
			for(var k = 0; k < 5; ++k) {
				if(letter[k*5 + j] == 1) {
					canvasContext.fillStyle = "white"
					canvasContext.fillRect(positionX,positionY,size,size);
					canvasContext.stroke();
				}
				positionY += size;
			}
			positionX += size;
		}
		positionX += spacing;
	}
};